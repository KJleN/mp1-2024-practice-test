#include <stdio.h>

size_t task1(int A[], size_t n) {
    if (n <= 1) {
        return 0; // Если в массиве 0 или 1 элемент, то нет пар соседних элементов
    }

    size_t count = 0;
    for (size_t i = 0; i < n - 1; i++) {
        if (A[i] < A[i + 1]) {
            count++;
        }
    }
    return count;
}

//в функции int main() создается уже вывод кол-ва чисел меньше своего правого соседа. Можно такжее рандомизировать n, и рандомизировать заполнение, но этого у меня не требуют 
